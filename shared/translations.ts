// Helper function to get translation
export function getTranslation(key: TranslationKey, language: 'en' | 'ar' = 'en'): string {
  if (language === 'ar') {
    return translations[key] || englishTranslations[key] || key;
  }
  return englishTranslations[key] || key;
}

// Helper function to format currency
export function formatCurrency(amount: number | string, language: 'en' | 'ar' = 'en'): string {
  const numAmount = typeof amount === 'string' ? parseFloat(amount) : amount;
  
  if (language === 'ar') {
    return `${numAmount.toFixed(3)} د.ك`;
  }
  return `KWD ${numAmount.toFixed(3)}`;
}

// Helper function to format number with Arabic numerals
export function formatNumber(num: number, language: 'en' | 'ar' = 'en'): string {
  if (language === 'ar') {
    const arabicNumerals = '٠١٢٣٤٥٦٧٨٩';
    return num.toString().replace(/[0-9]/g, (d) => arabicNumerals[parseInt(d)]);
  }
  return num.toString();
}